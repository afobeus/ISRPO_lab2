def area(a, h):
    """Returns area of a triangle with given side and height.
            Parameters:
                a (int): length of a side
                h (int): length of height passed to the side a"""
    return a * h / 2 


def perimeter(a, b, c):
    """Returns perimeter of a triangle with given sides.
            Parameters:
                a, b, c (int): length of three sides"""
    return a + b + c 
