def area(a, b):
    """Returns area of a rectangle with given sides.
        Parameters:
            a (int): length of one side
            b (int): length of second side"""
    return a * b


def perimeter(a, b):
    """Returns perimeter of a rectangle with given sides.
            Parameters:
                a (int): length of one side
                b (int): length of second side"""
    return 2 * (a + b)
